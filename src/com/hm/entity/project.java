package com.hm.entity;

public class project {
	private String proID;
	private String proName;
	private String region;
	private String leaderN;
	private float hours;
	private float score;
	private String memberNs;
	private int memCount;
	private String pc;
	public String getProID() {
		return proID;
	}
	public void setProID(String proID) {
		this.proID = proID;
	}
	public String getProName() {
		return proName;
	}
	public void setProName(String proName) {
		this.proName = proName;
	}
	public String getRegion() {
		return region;
	}
	public void setRegion(String region) {
		this.region = region;
	}
	public String getLeaderN() {
		return leaderN;
	}
	public void setLeaderN(String leaderN) {
		this.leaderN = leaderN;
	}
	public float getHours() {
		return hours;
	}
	public void setHours(float hours) {
		this.hours = hours;
	}
	public float getScore() {
		return score;
	}
	public void setScore(float score) {
		this.score = score;
	}
	public String getMemberNs() {
		return memberNs;
	}
	public void setMemberNs(String memberNs) {
		this.memberNs = memberNs;
	}
	public int getMemCount() {
		return memCount;
	}
	public void setMemCount(int memCount) {
		this.memCount = memCount;
	}
	public String getPc() {
		return pc;
	}
	public void setPc(String pc) {
		this.pc = pc;
	}
	
}
